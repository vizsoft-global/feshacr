}

bool checkStringContent(String inputString) {
  if (inputString == null) {
    return true;
  }

  // Trim the string to account for potential leading/trailing whitespace
  final cleanString = inputString.trim();

  // If the string is empty after trimming, it contains no disallowed characters (numbers).
  if (cleanString.isEmpty) {
    return true;
  }

  // Regular Expression to detect the presence of any numerical digit (0-9).
  // \d is a shorthand for [0-9].
  final numberRegex = RegExp(r'\d');

  // If the regex matches (meaning a digit is found), return false.
  // The string is considered invalid if it contains any number.
  if (numberRegex.hasMatch(cleanString)) {
    return false;
  }

  // If no digits are found, the string is valid (contains letters, punctuation, symbols, etc.).
  return true;
}
