String convertToEnglishNumerals(String phoneNumber) {
  if (phoneNumber == null || phoneNumber.isEmpty) {
    return '';
  }

  // Define a comprehensive map of non-Latin numerals to their English equivalents.
  final Map<String, String> numeralMap = {
    // Arabic-Indic Numerals
    '٠': '0', '١': '1', '٢': '2', '٣': '3', '٤': '4',
    '٥': '5', '٦': '6', '٧': '7', '٨': '8', '٩': '9',

    // Eastern Arabic / Persian Numerals
    '۰': '0', '۱': '1', '۲': '2', '۳': '3', '۴': '4',
    '۵': '5', '۶': '6', '۷': '7', '۸': '8', '۹': '9',

    // Devanagari Numerals (e.g., Hindi, Marathi)
    '०': '0', '१': '1', '२': '2', '३': '3', '٤': '4',
    '५': '5', '٦': '6', '७': '7', '८': '8', '९': '9',
  };

  String result = phoneNumber;

  // 1. Replace non-English numerals with their ASCII counterparts.
  numeralMap.forEach((nonEnglish, english) {
    result = result.replaceAll(nonEnglish, english);
  });

  // 2. Remove '+', '-', and spaces for a cleaner digit string.
  result = result.replaceAll('+', '').replaceAll('-', '').replaceAll(' ', '');

  return result;
}

