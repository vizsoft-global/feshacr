int calculateMinutesBetweenDatetimes(
  DateTime startDatetime,
  DateTime endDatetime,
) {
  final DateTime startDatetimeNormalized =
      startDatetime.copyWith(second: 0, millisecond: 0);
  final DateTime endDatetimeNormalized =
      endDatetime.copyWith(second: 0, millisecond: 0);

  // Calculate the time difference, which returns a Duration object.
  final Duration timeDifference =
      endDatetimeNormalized.difference(startDatetimeNormalized);

  // Get the total number of full minutes from the Duration object.
  return timeDifference.inMinutes;
}

DateTime? getFeatureTimeViaAddHurMin(
  DateTime date,
  int hur,
  int min,
) {
  DateTime futureTime = date.add(Duration(hours: hur));
  futureTime = futureTime.add(Duration(minutes: min));
  return futureTime;
}

